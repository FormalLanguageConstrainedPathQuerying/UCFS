    IDENTIFIER(102), EOF(-1), INTEGER_LITERAL(51), FLOATING_POINT_LITERAL(52), BOOLEAN_LITERAL(53), CHARACTER_LITERAL(54),
    STRING_LITERAL(55), NULL_LITERAL(56), BOOLEAN(3), BYTE(5), SHORT(37), INT(27), LONG(29), CHAR(8), FLOAT(20), DOUBLE(14), DOT(65),
    LBRACK(61), RBRACK(62), LPAREN(57), RPAREN(58), LBRACE(59), RBRACE(60), EXTENDS(17), AND(85), LT(68), GT(67), SEMICOLON(63), COLON(72),
    ELLIPSIS(104), COMMA(64), QUESTION(71), SUPER(40), PACKAGE(32), IMPORT(25), STATIC(38), MULT(83), PLUS(81), MINUS(82), MOD(88), DIV(84),
    PLUSPLUS(79), MINUSMINUS(80), COMP(70), NOT(69), CLASS(9), PUBLIC(35), PROTECTED(34), PRIVATE(33), FINAL(18), STRICTFP(39),
    IMPLEMENTS(24), TRANSIENT(46), VOLATILE(49), EQ(66), MULTEQ(93), DIVEQ(94), PLUSEQ(91), MINUSEQ(92), MODEQ(98), XOREQ(97), COLONCOLON(90),
